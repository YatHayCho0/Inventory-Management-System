    // Reads the lines from the file and stores them in an array
    Object[] lines = br.lines().toArray();
    
    // Creates a list to store the data
    List<String[]> dataList = new ArrayList<>();
    
    // Reads the data from the lines and stores it in the list
    for(int i = 0; i < lines.length; i++ ){
        String[] row = lines[i].toString().split("\\|");
        if (row.length >= 4) {
            dataList.add(row);
        }
    }
    
    // Sorts the data in the list
    Collections.sort(dataList, Comparator.comparing(row -> row[0]));
    
    // Adds the sorted data to the table
    for(String[] row : dataList) {
        String itemID = row[0];
        String itemName = row[1];
        int Boxes = Integer.parseInt(row[2]);
        
        Object[] rowData = {itemID, itemName, Boxes};
        
        model.addRow(rowData);
    }
